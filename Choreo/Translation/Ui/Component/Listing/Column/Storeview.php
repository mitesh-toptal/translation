namespace Choreo\Translation\Ui\Component\Listing\Column;

class Storeview extends \Magento\Ui\Component\Listing\Columns\Column
{
    protected $_storeManager;

    public function __construct(
        \Magento\Framework\View\Element\UiComponent\ContextInterface $context,
        \Magento\Framework\View\Element\UiComponentFactory $uiComponentFactory,
        array $components = [],
        array $data = [],
        \Magento\Store\Model\StoreManagerInterface $storeManager
    ) {
        $this->_storeManager = $storeManager;
        parent::__construct($context, $uiComponentFactory, $components, $data);
    }

    public function prepareDataSource(array $dataSource)
    {
        if (isset($dataSource['data']['items'])) {
            foreach ($dataSource['data']['items'] as &$item) {
                $storeInfo           = $this->_storeManager->getStore($item['sourcestore']);
                $item['sourcestore'] = $storeInfo->getData('name');
            }
        }
        return $dataSource;
    }
}
